#Perulangan While Baca BUku
jumlah_buku = 10
print('Ibu berkata "Baca semua bukumu"')

jumlah_buku_bacaan = 0
print(f'Jumlah buku yang sudah dibaca {jumlah_buku_bacaan}')

while jumlah_buku_bacaan < jumlah_buku :
    jumlah_buku_bacaan = jumlah_buku_bacaan + 1
    print(f"Buku ke {jumlah_buku_bacaan} sudah dibaca")

print(f'Jumlah buku yang sudah dibaca {jumlah_buku_bacaan}')
